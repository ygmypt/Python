# Создать пустой словарь для запоминания вопросов и ответов
memory = {}

def ответ_да_или_нет(вопрос):
    transformed_question = вопрос.lower().strip()  # Привести вопрос к нижнему регистру и удалить пробелы

    # Проверить, есть ли уже ответ на этот вопрос в памяти
    if transformed_question in memory:
        ответ = memory[transformed_question]
        print("Ответ (из памяти):", ответ)
    else:
        ответ = input("Введите ответ на вопрос (\"да\" или \"нет\"): ")
        memory[transformed_question] = ответ  # Запомнить вопрос и ответ в памяти
        print("Ответ:", ответ)

# Задать вопросы и получить ответы
ответ_да_или_нет("Будет ли завтра дождь?")
ответ_да_или_нет("Должен ли я сделать это задание?")
ответ_да_или_нет("Будешь помнить меня завтра?")

# Вывести все вопросы и ответы, запомненные в памяти
print("Запомненные вопросы и ответы:")
for question, answer in memory.items():
    print(question, "-", answer)
